/*
 * Author: Riley Radle
 * Description: 
 *      This enum represents the three different states that a tile 
 *      on the tic tac toe board can have: X, O, or N (not occupied)
 */

enum tile_state 
{
    O = -1, // Represents an X on the board
    N,      // Represents an O on the board
    X       // Represents an unoccupied tile on the board
};